
class Math:
    """Class for basic Math operations"""
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def add(self):
        """Add two numbers"""

        return self.a + self.b

    def subtract(self):
        """Subtract two numbers"""

        return self.a - self.b

    def multiply(self):
        """Multiply two numbers"""

        return self.a * self.b

    def divide(self):
        """Divide two numbers"""

        return self.a / self.b
